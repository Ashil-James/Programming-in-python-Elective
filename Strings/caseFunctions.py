s = "hello world"

print(s.upper())     # HELLO WORLD (All CAPS)
print(s.lower())     # hello world (all lowercase)
print(s.title())     # Hello World (Each Word Starts with a Capital Letter))
print(s.capitalize())# Hello world (First Letter of the First Word is Capitalized)
print(s.swapcase())  # HELLO WORLD → hello world (Convert uppercase to lowercase and vice versa)